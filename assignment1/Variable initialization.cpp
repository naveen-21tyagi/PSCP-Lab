#include <iostream>                       // includes iostream header file
using namespace std;                    

  
int main() {                            

	int a=21;                              //declares and assign value to a
	cout<<"\nThe value of a(int): "<<a;    //  print value of a           
	
	float b=21.21;                            //declares and assign value to b
    cout<<"\nThe value of b(float): "<<b;   //  print value of b
     
    char c='a';                              //declares and assign value to c
    cout<<"\nThe value of c(char): "<<c;     //  print value of c
    
    
    bool d=true;                             //declares and assign value to d
    cout<<"\nThe value of d(bool): "<<d;       //  print value of d
	
	wchar_t e=L'b';                               //declares and assign value to e
    cout<<"\nThe value of e(wide character): "<<e;//  print value of e
    
    double f=21.2121;                             //declares and assign value to f
    cout<<"\nThe value of f(double): "<<f;        //  print value of f
     
	return 0;            
}