#include <iostream>
using namespace std;
int main() {
   int a = 5, b = 6;         //  declaration of varable a,b and and assigning values
   cout<<"Value of a :"<<a;   //print value of a
   cout<<"\nValue of b :"<<b;  //print value of b
   
   swap(a, b);                 // swap function  to swap values of a and b
   cout<<"\n\nAfter swapping:"<<"\na="<<a<<"\nb="<<b;  // print values(after swapping) of a and b
   return 0;
}
